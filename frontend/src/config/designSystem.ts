// Utilitários para o sistema de design
export const getColor = (color: string, shade: keyof typeof designSystem.colors.primary = 500) => {
  const colorPath = color.split('.');
  if (colorPath.length === 1) {
    return designSystem.colors[colorPath[0] as keyof typeof designSystem.colors]?.[shade];
  }
  
  const colorName = colorPath[0] as keyof typeof designSystem.colors;
  const colorShade = colorPath[1] as keyof typeof designSystem.colors.primary;
  
  return designSystem.colors[colorName]?.[colorShade];
};

export const getSpacing = (size: keyof typeof designSystem.spacing) => {
  return designSystem.spacing[size];
};

export const getTypography = (type: keyof typeof designSystem.typography.fontSize) => {
  return designSystem.typography.fontSize[type];
};
